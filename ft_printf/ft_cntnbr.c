#include "ft_printf.h"

int	ft_cntnbr(int i)
{
	int	n;

	if (i == -2147483648)
		return (11);
	n = 0;
	if (i < 0)
	{
		n++;
		i *= -1;
	}
	while (i > 9)
	{
		i = i / 10;
		n++;
	}
	return (n + 1);
}
